function fn() {
	var i = 10;
	return function (n) {
		console.log(n + (++i));
	}
}
var f = fn();
f(10); // -> 10+ 1 + 10 = 21  （i=11）
f(20); // -> 20 + 1 + 11 = 32
fn()(10); // -> 10 + 10 +1 = 21
fn()(20); // -> 20 + 10 + 1 = 31
